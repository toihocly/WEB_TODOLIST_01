class THTable extends React.Component {
  state = {
    selected: [],
    page: 0,
    rowsPerPage: 5
  };

  handleRequestSort = (event, property) => {
    const orderBy = property;
    let order = "desc";

    if (this.props.orderBy === property && this.props.order === "desc") {
      order = "asc";
    }

    this.props.onHandleSort(orderBy, order);
  };

  handleSelectAllClick = event => {
    if (event.target.checked) {
      this.setState(state => ({
        selected: this.props.listData.map(n => n.id)
      }));
      return;
    }
    this.setState({ selected: [] });
  };

  handleClick = (event, item) => {
    const { selected } = this.state;
    const selectedIndex = selected.indexOf(item.id);
    let newSelected = [];

    if (selectedIndex === -1) {
      newSelected = newSelected.concat(selected, item.id);
    } else if (selectedIndex === 0) {
      newSelected = newSelected.concat(selected.slice(1));
    } else if (selectedIndex === selected.length - 1) {
      newSelected = newSelected.concat(selected.slice(0, -1));
    } else if (selectedIndex > 0) {
      newSelected = newSelected.concat(
        selected.slice(0, selectedIndex),
        selected.slice(selectedIndex + 1)
      );
    }
    
    let itemSelected ={
      isEdit : newSelected.length === 1,
      item:item
    }
    this.props.onHandleEdit(itemSelected);
    this.setState({ selected: newSelected });
  };



  handleChangePage = (event, page) => {
    this.setState({ page });
  };

  handleChangeRowsPerPage = event => {
    this.setState({ rowsPerPage: event.target.value });
  };

  isSelected = id => this.state.selected.indexOf(id) !== -1;

  showElementLevel = (level)=> {
    let elmLevel = <Chip className={this.props.classes.Chip} label="Small " color="default" />;
    if (level === 1) elmLevel = <Chip className={this.props.classes.Chip} label="Medium" color="primary" />;
    else if (level === 2) elmLevel = <Chip className={this.props.classes.Chip} label=" High " color="secondary" />;

    return elmLevel;
  }

  handleDelete = () => {
    //console.log('delete :: ',this.state.selected);
    this.props.onHandleDelete(this.state.selected);
  };

  componentWillReceiveProps(newProps) {
    if (newProps !== this.props) {
      this.setState({ selected: newProps.selected });
    }
    return true;
  }

  render() {
    const { classes, order, orderBy, listData } = this.props;
    const { selected, rowsPerPage, page } = this.state;
    const emptyRows =
      rowsPerPage - Math.min(rowsPerPage, listData.length - page * rowsPerPage);

    return (
      <Grid item xs={12}>
        <Paper className={classes.root}>
          <EnhancedTableToolbar
            numSelected={selected.length}
            onHandleDeleteButton={this.handleDelete}
          />
          <div className={classes.tableWrapper}>
            <Table className={classes.table} aria-labelledby="tableTitle">
              <EnhancedTableHead
                numSelected={selected.length}
                order={order}
                orderBy={orderBy}
                onSelectAllClick={this.handleSelectAllClick}
                onRequestSort={this.handleRequestSort}
                rowCount={listData.length}
              />
              <TableBody>
                {stableSort(listData, getSorting(order, orderBy))
                  .slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
                  .map(n => {
                    const isSelected = this.isSelected(n.id);
                    return (
                      <TableRow
                        hover
                        onClick={event => this.handleClick(event, n)}
                        role="checkbox"
                        aria-checked={isSelected}
                        tabIndex={-1}
                        key={n.id}
                        selected={isSelected}
                      >
                        <TableCell padding="checkbox">
                          <Checkbox checked={isSelected} />
                        </TableCell>
                        <TableCell component="th" scope="row" padding="none">
                          {n.name}
                        </TableCell>
                        <TableCell align="right">
                          {this.showElementLevel(n.level)}
                        </TableCell>
                      </TableRow>
                    );
                  })}
                {emptyRows > 0 && (
                  <TableRow style={{ height: 49 * emptyRows }}>
                    <TableCell colSpan={3} />
                  </TableRow>
                )}
              </TableBody>
            </Table>
          </div>
          <TablePagination
            rowsPerPageOptions={[5, 10, 25]}
            component="div"
            count={listData.length}
            rowsPerPage={rowsPerPage}
            page={page}
            backIconButtonProps={{
              "aria-label": "Previous Page"
            }}
            nextIconButtonProps={{
              "aria-label": "Next Page"
            }}
            onChangePage={this.handleChangePage}
            onChangeRowsPerPage={this.handleChangeRowsPerPage}
          />
        </Paper>
      </Grid>
    );
  }
}

THTable.propTypes = {
  classes: PropTypes.object.isRequired
};

export default withStyles(styles)(THTable);
